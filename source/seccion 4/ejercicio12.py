minutos_totales = 0
# Defino la variable para el tiempo total jugado en minutos
print("Ingrese el tiempo total jugado en minutos:")
minutos_totales = int(input())
# Pido el tiempo total jugado en minutos

horas = minutos_totales // 60
minutos = minutos_totales % 60
# Calculo las horas y minutos
print("Tiempo total de juego:", horas, "horas y", minutos, "minutos")
